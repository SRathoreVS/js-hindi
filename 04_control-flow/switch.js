const month = 3 

//key = is that value which we check each time , here it is month .
// case (value) = value is the thing which is assigned to that key and we need it 
switch (month) {
    case 1:
        console.log("january");
        break;
    case 2:
        console.log("february");
        break;
    case 3:
        console.log("march");
        break;
    case 4:
        console.log("April");
        break;

    default:
        console.log("default case match");
        break;
} // March

